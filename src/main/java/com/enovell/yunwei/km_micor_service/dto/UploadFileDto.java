public class UploadFileDto implements Serializable {

	private static final long serialVersionUID = 7863859406445977273L;

	/**
	 * 上传文件id
	 */
	private String uploadFileId;

	/**
	 * 文件名称
	 */
	private String name;

	/**
	 * 根据时间得到新文件名
	 */
	private String newName;

	/**
	 * 存储路径
	 */
	private String path;

	/**
	 * 上传时间
	 */
	private Date date;

	/**
	 * 是否存在
	 */
	private boolean is_Exist;

	public String getUploadFileId() {
		return uploadFileId;
	}

	public void setUploadFileId(String uploadFileId) {
		this.uploadFileId = uploadFileId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPath() {
		return path;
	}

	public void setPath(String path) {
		this.path = path;
	}

	public Date getDate() {
		return date;
	}

	public void setDate(Date date) {
		this.date = date;
	}

	public boolean isIs_Exist() {
		return is_Exist;
	}

	public void setIs_Exist(boolean is_Exist) {
		this.is_Exist = is_Exist;
	}

	public String getNewName() {
		return newName;
	}

	public void setNewName(String newName) {
		this.newName = newName;
	}

	@Override
	public String toString() {
		return "UploadFileDto [uploadFileId=" + uploadFileId + ", name=" + name + ", newName=" + newName + ", path="
				+ path + ", date=" + date + ", is_Exist=" + is_Exist + "]";
	}

}
