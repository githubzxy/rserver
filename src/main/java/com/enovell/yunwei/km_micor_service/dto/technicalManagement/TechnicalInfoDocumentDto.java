package com.enovell.yunwei.km_micor_service.dto.technicalManagement;

import java.io.Serializable;

public class TechnicalInfoDocumentDto implements Serializable{


	
	private static final long serialVersionUID = -6278459141269141205L;


	private String _id;
	
	
	/**
	 * 资料名称
	 */
	private String name;

	
	/**
	 * 线别
	 */
	private String railLine;
	/**
	 * 所属机房 
	 */
	private String machineRoom;

	
	/**
	 * 备注
	 */
	private String remark;

	/**
	 * 创建日期
	 */
	private String  createDate;
	
	/**
	 * 修改日期
	 */
	private String  updateDate;
	
	/**
	 * 创建人
	 */
	private String createUser;
	
	/**
	 * 修改人
	 */
	private String updateUser;
	
	/**
	 * 附件
	 */
	private String  uploadFile;

	/**
	 * 是否存在（true 存在 false 已被删除）
	 */
	private Boolean exist = true;

	public String get_id() {
		return _id;
	}

	public void set_id(String _id) {
		this._id = _id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getRailLine() {
		return railLine;
	}

	public void setRailLine(String railLine) {
		this.railLine = railLine;
	}

	public String getMachineRoom() {
		return machineRoom;
	}

	public void setMachineRoom(String machineRoom) {
		this.machineRoom = machineRoom;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	public String getCreateDate() {
		return createDate;
	}

	public void setCreateDate(String createDate) {
		this.createDate = createDate;
	}

	public String getUpdateDate() {
		return updateDate;
	}

	public void setUpdateDate(String updateDate) {
		this.updateDate = updateDate;
	}

	public String getCreateUser() {
		return createUser;
	}

	public void setCreateUser(String createUser) {
		this.createUser = createUser;
	}

	public String getUpdateUser() {
		return updateUser;
	}

	public void setUpdateUser(String updateUser) {
		this.updateUser = updateUser;
	}

	public String getUploadFile() {
		return uploadFile;
	}

	public void setUploadFile(String uploadFile) {
		this.uploadFile = uploadFile;
	}

	public Boolean getExist() {
		return exist;
	}

	public void setExist(Boolean exist) {
		this.exist = exist;
	}

	
	
}
