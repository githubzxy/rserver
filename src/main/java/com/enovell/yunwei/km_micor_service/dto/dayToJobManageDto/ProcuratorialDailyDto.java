package com.enovell.yunwei.km_micor_service.dto.dayToJobManageDto;

import java.io.Serializable;

public class ProcuratorialDailyDto implements Serializable {
	/**
	 * 用于导入检修日志Excel的dto
	 */
	private static final long serialVersionUID = -1686306364475979479L;
   /**
    * 填报车间
    */
	private String workshop;
	/**
	 * 填报部门
	 */
	private String department;
	/**
	 * 检查日期
	 */
	private String date;
	/**
	 * 检查人
	 */
	private String inspector;
	/**
	 * 检查地点
	 */
	private String site;
	/**
	 * 检查内容
	 */
	private String content;
	/**
	 *发现问题
	 */
	private String	problem;
	/**
	 * 整改要求
	 */
	private String require;
	/**
	 * 整改落实情况
	 */
	private String condition;
	/**
	 * 整改负责人
	 */
	private String functionary;
	/**
	 * 备注
	 */
	private String remark;
	public String getWorkshop() {
		return workshop;
	}
	public void setWorkshop(String workshop) {
		this.workshop = workshop;
	}
	public String getDepartment() {
		return department;
	}
	public void setDepartment(String department) {
		this.department = department;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getInspector() {
		return inspector;
	}
	public void setInspector(String inspector) {
		this.inspector = inspector;
	}
	public String getSite() {
		return site;
	}
	public void setSite(String site) {
		this.site = site;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getProblem() {
		return problem;
	}
	public void setProblem(String problem) {
		this.problem = problem;
	}
	public String getRequire() {
		return require;
	}
	public void setRequire(String require) {
		this.require = require;
	}
	public String getCondition() {
		return condition;
	}
	public void setCondition(String condition) {
		this.condition = condition;
	}
	public String getFunctionary() {
		return functionary;
	}
	public void setFunctionary(String functionary) {
		this.functionary = functionary;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	public static long getSerialversionuid() {
		return serialVersionUID;
	}
	@Override
	public String toString() {
		return "ProcuratorialDailyDto [workshop=" + workshop + ", department=" + department + ", date=" + date
				+ ", inspector=" + inspector + ", site=" + site + ", content=" + content + ", problem=" + problem
				+ ", require=" + require + ", condition=" + condition + ", functionary=" + functionary + ", remark="
				+ remark + "]";
	}
	
	
	
}
